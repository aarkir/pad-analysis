package com.aarkir.padanalysis;

class Pair {
    private int color;
    private double molarity;

    public int getColor() {
        return color;
    }

    double getMolarity() {
        return molarity;
    }

    Pair(int color, double molarity) {
        this.color = color;
        this.molarity = molarity;
    }
}
